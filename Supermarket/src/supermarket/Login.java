    private void LoginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginButtonMouseClicked
        if(UserIDField.getText().isEmpty() || UserPasswordField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing information");
        }
        else
        {
            if(UserRoleComboBox.getSelectedItem().toString().equals("Seller")){
                String Query = "SELECT * FROM SELLERS WHERE NAME='"+UserIDField.getText()+"' AND PASSWORD='"+UserPasswordField.getText()+"'";
                try{
                    con = DriverManager.getConnection("jdbc:derby://localhost:1527/Supermarket","Employee","1234");
                    st = con.createStatement();
                    rs = st.executeQuery(Query);
                    if(rs.next()){
                        new Selling().setVisible(true);
                        this.dispose();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Wrong seller ID or password");
                    }
                } catch(SQLException e){
                    e.printStackTrace();
                }
            } else {
                String Query = "SELECT * FROM ADMIN WHERE NAME='"+UserIDField.getText()+"' AND PASSWORD='"+UserPasswordField.getText()+"'";
                try{
                    con = DriverManager.getConnection("jdbc:derby://localhost:1527/Supermarket","Employee","1234");
                    st = con.createStatement();
                    rs = st.executeQuery(Query);
                    if(rs.next()){
                        new Products().setVisible(true);
                        this.dispose();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Wrong admin ID or password");
                    }
                } catch(SQLException e){
                    e.printStackTrace();
                }
            }
        }
        
        
    }//GEN-LAST:event_LoginButtonMouseClicked

